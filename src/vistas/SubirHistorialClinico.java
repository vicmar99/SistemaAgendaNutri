    private void btnCargarArchivoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCargarArchivoActionPerformed
        String idPaciente = txtIdPacienteHC.getText();
        String nombrePaciente = txtPacienteNombre.getText();

        if (wordFile != null && idPaciente != null && !idPaciente.isEmpty() && nombrePaciente != null && !nombrePaciente.isEmpty()) {
            try {
                FileSystemView fileSystemView = FileSystemView.getFileSystemView();
                File documentsFolder = fileSystemView.getDefaultDirectory();
                File historialesClinicosFolder = new File(documentsFolder, "historialesclinicos");

                if (!historialesClinicosFolder.exists()) {
                    historialesClinicosFolder.mkdir();
                }

                String nuevoNombreArchivo = "H C " + nombrePaciente + idPaciente + ".docx";  // Nuevo formato de nombre
                File destino = new File(historialesClinicosFolder, nuevoNombreArchivo);
                Files.copy(wordFile.toPath(), destino.toPath(), StandardCopyOption.REPLACE_EXISTING);

                HistorialClinico historialClinico = new HistorialClinico();
                historialClinico.setPacienteId(Integer.parseInt(idPaciente));
                historialClinico.setArchivoNombre(nuevoNombreArchivo);
                historialClinico.setArchivoReferencia(destino.getAbsolutePath());

                HistorialClinicoDAOImpl historialClinicoDAO = new HistorialClinicoDAOImpl();
                historialClinicoDAO.agregar(historialClinico);

                Dashboard.mostrarMensajeAviso("¡Historial clínico subido de forma exitosa!");
                vistas.Pacientes pacientes = new Pacientes();
                showView(pacientes);

            } catch (Exception e) {
                e.printStackTrace();
                Dashboard.mostrarMensajeError("Ha ocurrido un error");
            }
        } else {
            Dashboard.mostrarMensajeAlerta("Debe completar toda la información solicitada");
        }
    }//GEN-LAST:event_btnCargarArchivoActionPerformed

