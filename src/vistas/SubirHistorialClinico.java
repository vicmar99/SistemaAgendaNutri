    private void btnSeleccionarArchivoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSeleccionarArchivoActionPerformed
        JFileChooser fileChooser = new JFileChooser();

        // Agregar un filtro para archivos de Word (DOCX)
        FileNameExtensionFilter wordFilter = new FileNameExtensionFilter("Archivos de Word", "docx");
        fileChooser.setFileFilter(wordFilter);

        int result = fileChooser.showOpenDialog(this);

        if (result == JFileChooser.APPROVE_OPTION) {
            File selectedFile = fileChooser.getSelectedFile();

            // Verificar que el archivo seleccionado sea un archivo de Word (DOCX)
            if (selectedFile.getName().toLowerCase().endsWith(".docx")) {
                wordFile = selectedFile;

                // Cambiar el texto del botón para mostrar el nombre del archivo seleccionado
                btnSeleccionarArchivo.setText(selectedFile.getName());
            } else {
                JOptionPane.showMessageDialog(this, "Selecciona un archivo de Word válido (DOCX).");
            }
        }
    }//GEN-LAST:event_btnSeleccionarArchivoActionPerformed

