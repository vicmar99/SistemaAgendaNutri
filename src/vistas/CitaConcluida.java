    private void btnSeleccionarArchivoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSeleccionarArchivoActionPerformed
        JFileChooser fileChooser = new JFileChooser();

        // Agregar un filtro para archivos PDF
        FileNameExtensionFilter pdfFilter = new FileNameExtensionFilter("Archivos PDF", "pdf");
        fileChooser.setFileFilter(pdfFilter);

        int result = fileChooser.showOpenDialog(this);

        if (result == JFileChooser.APPROVE_OPTION) {
            File selectedFile = fileChooser.getSelectedFile();

            // Verificar que el archivo seleccionado sea PDF
            if (selectedFile.getName().toLowerCase().endsWith(".pdf")) {
                pdfFile = selectedFile;

                // Cambiar el texto del bot√≥n para mostrar el nombre del archivo seleccionado
                btnSeleccionarArchivo.setText(selectedFile.getName());
            } else {
                JOptionPane.showMessageDialog(this, "Selecciona un archivo PDF v√°lido.");
            }
        }
    }//GEN-LAST:event_btnSeleccionarArchivoActionPerformed

    private void btnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarActionPerformed
        String idCita = txtIdCita.getText();
        String observaciones = txtAnotaciones.getText();

        if (pdfFile != null && idCita != null && !idCita.isEmpty() && observaciones != null && !observaciones.isEmpty()) {
            try {
                FileSystemView fileSystemView = FileSystemView.getFileSystemView();
                File documentsFolder = fileSystemView.getDefaultDirectory();
                File archivosSeguimientoFolder = new File(documentsFolder, "archivosseguimiento");

                if (!archivosSeguimientoFolder.exists()) {
                    archivosSeguimientoFolder.mkdir();
                }

                String nuevoNombreArchivo = pdfFile.getName().replace(".pdf", "") + "_" + idCita + ".pdf"; // Nuevo nombre
                File destino = new File(archivosSeguimientoFolder, nuevoNombreArchivo); // Usar el nuevo nombre
                Files.copy(pdfFile.toPath(), destino.toPath(), StandardCopyOption.REPLACE_EXISTING);

                SeguimientoCita seguimientoCita = new SeguimientoCita();
                seguimientoCita.setCitaId(Integer.parseInt(idCita));
                seguimientoCita.setObservaciones(observaciones);
                seguimientoCita.setNombreArchivo(nuevoNombreArchivo); // Actualizar el nombre del archivo
                seguimientoCita.setRutaArchivo(destino.getAbsolutePath());

                SeguimientoCitaDAOImpl seguimientoCitaDAO = new SeguimientoCitaDAOImpl();
                seguimientoCitaDAO.agregar(seguimientoCita);

                // Cambiar el estado de la cita a "Concluida"
                CitaDAO citaDAO = new CitaDAOImplm();
                Cita citaBD = citaDAO.traerCita(idCita);
                String nuevoEstado = "Concluida";
                citaBD.setEstado(nuevoEstado);
                citaDAO.actualizar(citaBD);

                // Informar al usuario sobre la confirmaci√≥n exitosa
                Dashboard.mostrarMensajeAviso("Operaci√≥n exitosa");

                vistas.Citas citas = new Citas();
                showView(citas);
            } catch (Exception e) {
                e.printStackTrace();
                // Manejar el error de manera apropiada
            }
        } else {
            // Mostrar un mensaje al usuario indicando que debe completar todos los campos
            Dashboard.mostrarMensajeAlerta("Completa todos los campos antes de confirmar");
        }

    }//GEN-LAST:event_btnConfirmarActionPerformed

