    private void btnReagendar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReagendar1ActionPerformed
        String fechaStr = String.valueOf(boxFecha.getSelectedItem());
        String hora = String.valueOf(boxHora.getSelectedItem());
        String estado = "Pendiente";
        String idCita = txtNoCita.getText();

        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("EEEE dd/MM/yyyy");
        LocalDate localDate = LocalDate.parse(fechaStr, formatter);

        // Convert LocalDate to java.sql.Date
        java.sql.Date sqlDate = java.sql.Date.valueOf(localDate);

        Cita cita = new Cita();
        cita.setEstado(estado);
        cita.setFecha(sqlDate); // Aqu√≠ estableces el objeto java.sql.Date
        cita.setHora(hora);
        cita.setCitaId(Integer.parseInt(idCita));

        CitaDAO citaDAO = new CitaDAOImplm();
        try {
            citaDAO.actualizar(cita);
            Dashboard.mostrarMensajeAviso("La cita ha sido reagendada con exito");

            vistas.Citas citas = new Citas();
            showView(citas);
        } catch (Exception ex) {
            Logger.getLogger(NuevaCita.class.getName()).log(Level.SEVERE, null, ex);
            Dashboard.mostrarMensajeError("No se pudo realizar la operaci√≥n");
        }


    }//GEN-LAST:event_btnReagendar1ActionPerformed

