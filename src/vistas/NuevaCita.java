    private void rellenarFechas() {
        LocalDate currentDate = LocalDate.now();
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("EEEE dd/MM/yyyy"); // Agregamos "EEEE" para el día de la semana
        DefaultComboBoxModel<String> fechaModel = new DefaultComboBoxModel<>();

        for (int i = 0; i < 34; i++) {
            fechaModel.addElement(currentDate.format(formatter));
            currentDate = currentDate.plusDays(1);
        }

        boxFecha.setModel(fechaModel);
    }

