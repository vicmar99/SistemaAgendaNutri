    private void btnAgendarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgendarActionPerformed

        String fechaStr = String.valueOf(boxFecha.getSelectedItem());
        String hora = String.valueOf(boxHora.getSelectedItem());
        String estado = "Pendiente";
        String idPaciente = txtIdPaciente.getText();

        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("EEEE dd/MM/yyyy");
        LocalDate localDate = LocalDate.parse(fechaStr, formatter);

        // Convert LocalDate to java.sql.Date
        java.sql.Date sqlDate = java.sql.Date.valueOf(localDate);

        Cita cita = new Cita();
        cita.setEstado(estado);
        cita.setFecha(sqlDate); // Aquí estableces el objeto java.sql.Date
        cita.setHora(hora);
        cita.setPacienteId(Integer.parseInt(idPaciente));

        CitaDAO citaDAO = new CitaDAOImplm();
        try {
            List<Cita> citas = citaDAO.listar();

            for (Cita cita1 : citas) {

                if (sqlDate.equals(cita1.getFecha()) && hora.equalsIgnoreCase(cita1.getHora()) && estado.equalsIgnoreCase(cita1.getEstado())) {
                    Dashboard.mostrarMensajeAlerta("Ya se ha agendado una cita en esa fecha y hora, verifique.");
                    return;
                }
            }

            citaDAO.agregar(cita);
            Dashboard.mostrarMensajeAviso("La cita ha sido agendada con exito");

        } catch (Exception ex) {
            Logger.getLogger(NuevaCita.class.getName()).log(Level.SEVERE, null, ex);
            Dashboard.mostrarMensajeError("No se pudo realizar la operación");
        }

        vistas.Citas citas = new Citas();
        showView(citas);

    }//GEN-LAST:event_btnAgendarActionPerformed

